package ncr.inventory.data;

public class NetworkAdapterSetting
{
	private String name				= null;
	private String manufacturer			= null;
	private String adapterType		= null;
	private String caption		= null;
	private String description			= null;
	private String dhcpEnabled		= null;
	private String dhcpServer		= null;
	private String ipAddress		= null;
	private String ipSubnet		= null;
	private String defaultIPGateway		= null;
	private String macAddress			= null;
	private String speed		= null;
	private String netConnectionID		= null;
	private String netConnectionStatus		= null;
	private String interfaceIndex		= null;
	private String status		= null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getAdapterType() {
		return adapterType;
	}
	public void setAdapterType(String adapterType) {
		this.adapterType = adapterType;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDhcpEnabled() {
		return dhcpEnabled;
	}
	public void setDhcpEnabled(String dhcpEnabled) {
		this.dhcpEnabled = dhcpEnabled;
	}
	public String getDhcpServer() {
		return dhcpServer;
	}
	public void setDhcpServer(String dhcpServer) {
		this.dhcpServer = dhcpServer;
	}
	public String getIpAddress() {
		return ipAddress;
	}
	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}
	public String getIpSubnet() {
		return ipSubnet;
	}
	public void setIpSubnet(String ipSubnet) {
		this.ipSubnet = ipSubnet;
	}
	public String getDefaultIPGateway() {
		return defaultIPGateway;
	}
	public void setDefaultIPGateway(String defaultIPGateway) {
		this.defaultIPGateway = defaultIPGateway;
	}
	public String getMacAddress() {
		return macAddress;
	}
	public void setMacAddress(String macAddress) {
		this.macAddress = macAddress;
	}
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getNetConnectionID() {
		return netConnectionID;
	}
	public void setNetConnectionID(String netConnectionID) {
		this.netConnectionID = netConnectionID;
	}
	public String getNetConnectionStatus() {
		return netConnectionStatus;
	}
	public void setNetConnectionStatus(String netConnectionStatus) {
		this.netConnectionStatus = netConnectionStatus;
	}
	public String getInterfaceIndex() {
		return interfaceIndex;
	}
	public void setInterfaceIndex(String interfaceIndex) {
		this.interfaceIndex = interfaceIndex;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}

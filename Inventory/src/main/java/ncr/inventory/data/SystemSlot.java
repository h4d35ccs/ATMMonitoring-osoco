package ncr.inventory.data;

public class SystemSlot
{
	private String name				= null;
	private String manufacturer			= null;
	private String caption		= null;
	private String currentUsage		= null;
	private String serialNumber			= null;
	private String slotDesignation		= null;
	private String tag		= null;
	private String version		= null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getCurrentUsage() {
		return currentUsage;
	}
	public void setCurrentUsage(String currentUsage) {
		this.currentUsage = currentUsage;
	}
	public String getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getSlotDesignation() {
		return slotDesignation;
	}
	public void setSlotDesignation(String slotDesignation) {
		this.slotDesignation = slotDesignation;
	}
	public String getTag() {
		return tag;
	}
	public void setTag(String tag) {
		this.tag = tag;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	
	
	
}
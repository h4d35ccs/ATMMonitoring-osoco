package ncr.inventory.data;

public class Product {
    private String name = null;
    private String caption = null;
    private String description = null;
    private String identifyingNumber = null;
    private String language = null;
    private String installDate = null;
    private String localPackage = null;
    private String packageName = null;
    private String productID = null;
    private String vendor = null;
    private String version = null;
    private String swtype = null;

    public void setName(String name) {
	this.name = name;
    }

    public void setCaption(String caption) {
	this.caption = caption;
    }

    public void setDescription(String description) {
	this.description = description;
    }

    public void setIdentifyingNumber(String identifyingNumber) {
	this.identifyingNumber = identifyingNumber;
    }

    public void setLanguage(String language) {
	this.language = language;
    }

    public void setInstallDate(String installDate) {
	this.installDate = installDate;
    }

    public void setLocalPackage(String localPackage) {
	this.localPackage = localPackage;
    }

    public void setPackageName(String packageName) {
	this.packageName = packageName;
    }

    public void setProductID(String productID) {
	this.productID = productID;
    }

    public void setVendor(String vendor) {
	this.vendor = vendor;
    }

    public void setVersion(String version) {
	this.version = version;
    }

    public void setSWType(String swtype) {
	this.swtype = swtype;
    }

    public String getName() {
	return name;
    }

    public String getCaption() {
	return caption;
    }

    public String getDescription() {
	return description;
    }

    public String getIdentifyingNumber() {
	return identifyingNumber;
    }

    public String getLanguage() {
	return language;
    }

    public String getInstallDate() {
	return installDate;
    }

    public String getLocalPackage() {
	return localPackage;
    }

    public String getPackageName() {
	return packageName;
    }

    public String getProductID() {
	return productID;
    }

    public String getVendor() {
	return vendor;
    }

    public String getVersion() {
	return version;
    }

    public String getSwtype() {
	return swtype;
    }
}

package ncr.inventory.data;

public class PointingDevice
{
	private String name				= null;
	private String manufacturer			= null;
	private String caption		= null;
	private String description		= null;
	private String hardwareType			= null;
	private String pointingType		= null;
	private String status		= null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getHardwareType() {
		return hardwareType;
	}
	public void setHardwareType(String hardwareType) {
		this.hardwareType = hardwareType;
	}
	public String getPointingType() {
		return pointingType;
	}
	public void setPointingType(String pointingType) {
		this.pointingType = pointingType;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}

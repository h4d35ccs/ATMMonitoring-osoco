package ncr.inventory.data;

public class ComputerSystem
{
	private String name				= null;
	private String manufacturer			= null;
	private String model		= null;
	private String numberOfProcessors		= null;
	private String caption			= null;
	private String totalPhysicalMemory		= null;
	private String description		= null;
	private String currentTimeZone		= null;
	private String daylightInEffect		= null;
	private String domain			= null;
	private String workgroup		= null;
	private String status		= null;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getNumberOfProcessors() {
		return numberOfProcessors;
	}
	public void setNumberOfProcessors(String numberOfProcessors) {
		this.numberOfProcessors = numberOfProcessors;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getTotalPhysicalMemory() {
		return totalPhysicalMemory;
	}
	public void setTotalPhysicalMemory(String totalPhysicalMemory) {
		this.totalPhysicalMemory = totalPhysicalMemory;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getCurrentTimeZone() {
		return currentTimeZone;
	}
	public void setCurrentTimeZone(String currentTimeZone) {
		this.currentTimeZone = currentTimeZone;
	}
	public String getDaylightInEffect() {
		return daylightInEffect;
	}
	public void setDaylightInEffect(String daylightInEffect) {
		this.daylightInEffect = daylightInEffect;
	}
	public String getDomain() {
		return domain;
	}
	public void setDomain(String domain) {
		this.domain = domain;
	}
	public String getWorkgroup() {
		return workgroup;
	}
	public void setWorkgroup(String workgroup) {
		this.workgroup = workgroup;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
	
}
package ncr.inventory.data;

public class DiskDrive
{
	private String name				= null;
	private String manufacturer			= null;
	private String model		= null;
	private String caption		= null;
	private String description			= null;
	private String deviceID		= null;
	private String firmwareRevision		= null;
	private String serialNumber		= null;
	private String mediaType		= null;
	private String maxMediaSize		= null;
	private String size			= null;
	private String totalCylinders		= null;
	private String tracksPerCylinder		= null;
	private String sectorsPerTrack		= null;
	private String bytesPerSector		= null;
	private String partitions		= null;
	private String signature		= null;
	private String status		= null;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getDeviceID() {
		return deviceID;
	}
	public void setDeviceID(String deviceID) {
		this.deviceID = deviceID;
	}
	public String getFirmwareRevision() {
		return firmwareRevision;
	}
	public void setFirmwareRevision(String firmwareRevision) {
		this.firmwareRevision = firmwareRevision;
	}
	public String getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getMediaType() {
		return mediaType;
	}
	public void setMediaType(String mediaType) {
		this.mediaType = mediaType;
	}
	public String getMaxMediaSize() {
		return maxMediaSize;
	}
	public void setMaxMediaSize(String maxMediaSize) {
		this.maxMediaSize = maxMediaSize;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public String getTotalCylinders() {
		return totalCylinders;
	}
	public void setTotalCylinders(String totalCylinders) {
		this.totalCylinders = totalCylinders;
	}
	public String getTracksPerCylinder() {
		return tracksPerCylinder;
	}
	public void setTracksPerCylinder(String tracksPerCylinder) {
		this.tracksPerCylinder = tracksPerCylinder;
	}
	public String getSectorsPerTrack() {
		return sectorsPerTrack;
	}
	public void setSectorsPerTrack(String sectorsPerTrack) {
		this.sectorsPerTrack = sectorsPerTrack;
	}
	public String getBytesPerSector() {
		return bytesPerSector;
	}
	public void setBytesPerSector(String bytesPerSector) {
		this.bytesPerSector = bytesPerSector;
	}
	public String getPartitions() {
		return partitions;
	}
	public void setPartitions(String partitions) {
		this.partitions = partitions;
	}
	public String getSignature() {
		return signature;
	}
	public void setSignature(String signature) {
		this.signature = signature;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}
package ncr.inventory.data;

public class DesktopMonitor
{
	private String name				= null;
	private String caption			= null;
	private String deviceID		= null;
	private String displayType		= null;
	private String monitorManufacturer			= null;
	private String monitorType		= null;
	private String pixelsPerXLogicalInch		= null;
	private String pixelsPerYLogicalInch		= null;
	private String status		= null;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getDeviceID() {
		return deviceID;
	}
	public void setDeviceID(String deviceID) {
		this.deviceID = deviceID;
	}
	public String getDisplayType() {
		return displayType;
	}
	public void setDisplayType(String displayType) {
		this.displayType = displayType;
	}
	public String getMonitorManufacturer() {
		return monitorManufacturer;
	}
	public void setMonitorManufacturer(String monitorManufacturer) {
		this.monitorManufacturer = monitorManufacturer;
	}
	public String getMonitorType() {
		return monitorType;
	}
	public void setMonitorType(String monitorType) {
		this.monitorType = monitorType;
	}
	public String getPixelsPerXLogicalInch() {
		return pixelsPerXLogicalInch;
	}
	public void setPixelsPerXLogicalInch(String pixelsPerXLogicalInch) {
		this.pixelsPerXLogicalInch = pixelsPerXLogicalInch;
	}
	public String getPixelsPerYLogicalInch() {
		return pixelsPerYLogicalInch;
	}
	public void setPixelsPerYLogicalInch(String pixelsPerYLogicalInch) {
		this.pixelsPerYLogicalInch = pixelsPerYLogicalInch;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}

package ncr.inventory.data;

public class Bios
{
	private String name				= null;
	private String manufacturer			= null;
	private String smBIOSVersion		= null;
	private String smBIOSMajorVersion		= null;
	private String smBIOSMinorVersion			= null;
	private String caption		= null;
	private String currentLanguage		= null;
	private String primaryBIOS		= null;
	private String releaseDate		= null;
	private String serialNumber		= null;
	private String smBIOSPresent			= null;
	private String version		= null;
	private String status		= null;
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getManufacturer() {
		return manufacturer;
	}
	public void setManufacturer(String manufacturer) {
		this.manufacturer = manufacturer;
	}
	public String getSmBIOSVersion() {
		return smBIOSVersion;
	}
	public void setSmBIOSVersion(String smBIOSVersion) {
		this.smBIOSVersion = smBIOSVersion;
	}
	public String getSmBIOSMajorVersion() {
		return smBIOSMajorVersion;
	}
	public void setSmBIOSMajorVersion(String smBIOSMajorVersion) {
		this.smBIOSMajorVersion = smBIOSMajorVersion;
	}
	public String getSmBIOSMinorVersion() {
		return smBIOSMinorVersion;
	}
	public void setSmBIOSMinorVersion(String smBIOSMinorVersion) {
		this.smBIOSMinorVersion = smBIOSMinorVersion;
	}
	public String getCaption() {
		return caption;
	}
	public void setCaption(String caption) {
		this.caption = caption;
	}
	public String getCurrentLanguage() {
		return currentLanguage;
	}
	public void setCurrentLanguage(String currentLanguage) {
		this.currentLanguage = currentLanguage;
	}
	public String getPrimaryBIOS() {
		return primaryBIOS;
	}
	public void setPrimaryBIOS(String primaryBIOS) {
		this.primaryBIOS = primaryBIOS;
	}
	public String getReleaseDate() {
		return releaseDate;
	}
	public void setReleaseDate(String releaseDate) {
		this.releaseDate = releaseDate;
	}
	public String getSerialNumber() {
		return serialNumber;
	}
	public void setSerialNumber(String serialNumber) {
		this.serialNumber = serialNumber;
	}
	public String getSmBIOSPresent() {
		return smBIOSPresent;
	}
	public void setSmBIOSPresent(String smBIOSPresent) {
		this.smBIOSPresent = smBIOSPresent;
	}
	public String getVersion() {
		return version;
	}
	public void setVersion(String version) {
		this.version = version;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	
}